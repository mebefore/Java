package com.yedam.member;






public class Member {
	
	private String memberName; //이름
	private String memberId; // 아이디
	private String memberPw; //비번
	private String memberNum; //폰번
	private String memberAddr; //주소
	private String memberSchool; //학교
	private String memberType; // 학생인지 관리자인지
	
	
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberPw() {
		return memberPw;
	}
	public void setMemberPw(String memberPw) {
		this.memberPw = memberPw;
	}
	public String getMemberNum() {
		return memberNum;
	}
	public void setMemberNum(String memberNum) {
		this.memberNum = memberNum;
	}
	public String getMemberAddr() {
		return memberAddr;
	}
	public void setMemberAddr(String memberAddr) {
		this.memberAddr = memberAddr;
	}
	public String getMemberSchool() {
		return memberSchool;
	}
	public void setMemberSchool(String memberSchool) {
		this.memberSchool = memberSchool;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	
	
	

	
	
	
	
	
	
	
	
	
	
	
	
}